#Operações matemáticas (aritméticas)

'''
- Soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisão inteira: //
- Resto da divisão: %
- Potência: **
'''
num1 = 10
num2 = 20
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(20 % 3)
print(2 ** 3)

var1 = 12
var2 = 30
var3 = (var1 + var2)
print(var3)

var3 = (var3 / 2)
print(var3) 

